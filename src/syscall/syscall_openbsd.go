func Getfsstat(buf []Statfs_t, flags int) (n int, err error) {
	var bufptr *Statfs_t
	var bufsize uintptr
	if len(buf) > 0 {
		bufptr = &buf[0]
		bufsize = unsafe.Sizeof(Statfs_t{}) * uintptr(len(buf))
	}
	return getfsstat(bufptr, bufsize, flags)
}

